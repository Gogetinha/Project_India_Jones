int modulo(int i)
{

	if (i < 0)
		return (i * -1);
	else
		return i;
}

void Delay (unsigned int Segs)
{
	clock_t tp_ft = Segs + clock();
	while (tp_ft > clock());
}

char entrada_valida()
{
    int ch;
    struct termios oldt;
    struct termios newt;
    tcgetattr(STDIN_FILENO,&oldt);
    newt = oldt;
    newt.c_lflag &= ~(ICANON | ECHO);
    tcsetattr(STDIN_FILENO, TCSANOW, &newt);
    ch = getchar();
    tcsetattr(STDIN_FILENO, TCSANOW, &oldt);
    return ch;
}

